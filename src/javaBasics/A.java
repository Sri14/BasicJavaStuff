package javaBasics;

public class A {
	
	public A(){
		System.out.println("Parent class");
	}
	
	public A(int i){
		System.out.println("Parent class with value of i  "+i);
	}
	
	public A(int a , int b){
		System.out.println("Parent class with value of a  "+a);
		System.out.println("Parent class with value of b  "+b);

	}
}
