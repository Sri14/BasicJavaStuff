package javaBasics;

public class LoopsConcept {

	public static void main(String[] args) {
	
		//while loop printing numbers 1 to 10
		
		int a=1;
		while(a<=10){
			System.out.println(a);
			a++;
		}
		
		System.out.println("*****************");
		
		//while loop printing numebrs 10 to 1
		
		int b=10;
		while(b>=1){
			System.out.println(b);
			b--;
		}
		
		System.out.println("*****************");

		//for loop printing number 1 to 10
		
		for(int c=1; c<=10; c++){
			System.out.println(c);
		}

		System.out.println("*****************");

		for(int d=10; d>=1; d--){
			System.out.println(d);
			
		}
		
	}	
}
	


